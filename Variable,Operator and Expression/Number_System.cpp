#include <iostream>
using namespace std;

int main()
{
    /*
    Octal Number startwith 0 
    Hexadecimal Number start with 0x

    Both of these number will be converted to decimal number while printing
    */
    cout << "Decimal Number represent by octal 271 is " << 0271 << endl;
    cout << "Decimal Number represent by hexadecimal number 4a213 " << 0x4a213b << endl;
    return 0;
}